public class BooleanAlgebra {

    public static void main(String[] args) {

        int i;

        boolean a = true;
        boolean b = false;

        System.out.print("&& : ");
        System.out.println(a && b);

        System.out.print("|| : ");
        System.out.println(a || b);

        System.out.print("^ : ");
        System.out.println(a ^ b);

        System.out.print("a && !b : ");
        System.out.println(a && !b);

        System.out.print("!(a && b) : ");
        System.out.println(!(a && b));






    }
}
